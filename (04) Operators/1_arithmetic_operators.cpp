#include <iostream>
using namespace std;
int main()
{
    cout << 9 + 5 << endl;
    cout << 9 - 5 << endl;
    cout << 9 * 5 << endl;
    cout << 9 / 5 << endl;
    cout << 9 % 5 << endl;
    cout << 'a' + 1 << endl;
    cout << 'a' + 'b' << endl;
    cout << 'a' - 'b' << endl;
    cout << 'a' / 3 << endl;
    cout << 'a' % 3 << endl;

    return 0;
}

